
public class Switch {

	public static void main(String[] args) {
		
		int a=2;
		System.out.println("Printing the numbers in word(1-3)\n");
		
		switch(a) {
		  case 1:
			  System.out.println("One");
		    break;
		  case 2:
			  System.out.println("Two");    
			break;
		  case 3:
			  System.out.println("Three");
		    break;
		  default:
			  System.out.println("unknown");
		    
		}
		
	}

}
